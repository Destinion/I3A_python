#Soubory (Set) a slovníky (Dictionary)
#Soubory (Set) a slovníky (Dictionary) jsou datové typy, které jsou velmi podobné seznamům.
#Soubory (Set) jsou kolekce, které nejsou uspořádané a nemají indexy.
#Slovníky jsou kolekce objektů, které jsou neuspořádané, neměnné a nemohou se opakovat.
#Slovníky mají indexy, které jsou klíče, které jsou vždy řetězce.
#Soubory (Set) a slovníky jsou velmi užitečné, když potřebujete pracovat s velkými množstvími dat.

#Soubory (Set)
set = {"jablko", "banán", "čokoláda"}
print(set)

#Slovníky (Dictionary)
dict = {
    "značka": "Ford",
    "model": "Mustang",
    "rok": 1964
}
print(dict)





